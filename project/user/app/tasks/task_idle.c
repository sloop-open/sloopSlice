#include "common.h"

void task_idle(void)
{
    /* 初次进入任务时，执行一次 */
    SL_INIT;

    /* 任务结束，不再执行时，释放资源 */
    SL_FREE;

    /* 下方开始进入任务运行逻辑 */
    SL_RUN;
}

